// Create a context for the drag handle
export interface RowDragHandleContextType {
  attributes: Record<string, any>
  listeners: Record<string, any> | undefined
}

export const RowDragHandleContext = React.createContext<RowDragHandleContextType>({
  attributes: {},
  listeners: undefined,
})

// Create a separate component for the drag handle
export function DragHandle() {
  const { attributes, listeners } = React.useContext(RowDragHandleContext)

  return (
    <Button
      {...attributes}
      {...listeners}
      variant="ghost"
      size="icon"
      className="text-muted-foreground size-7 hover:bg-transparent cursor-grab active:cursor-grabbing"
    >
      <IconGripVertical className="text-muted-foreground size-3" />
      <span className="sr-only">Drag to reorder</span>
    </Button>
  )
}

